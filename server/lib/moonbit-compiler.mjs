import fs from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { spawn } from "node:child_process";
import { TEMP_ROOT } from "./config.mjs";

const MODULE_NAME = "moonap_generated/runtime";

function moonModuleJson() {
  return JSON.stringify(
    {
      name: MODULE_NAME,
      version: "0.1.0",
      readme: "README.md",
      repository: "",
      license: "Apache-2.0",
      keywords: ["moonap", "wasm", "moonbit"],
      description: "Generated by MoonAP",
    },
    null,
    2,
  );
}

function mainPackageJson() {
  return `options(
  "is-main": true,
)`;
}

async function ensureTemplateFiles(projectDir, moonbitCode) {
  await fs.mkdir(path.join(projectDir, "cmd", "main"), { recursive: true });
  await fs.writeFile(path.join(projectDir, "moon.mod.json"), moonModuleJson(), "utf8");
  await fs.writeFile(path.join(projectDir, "moon.pkg"), "// Generated by MoonAP\n", "utf8");
  await fs.writeFile(path.join(projectDir, "README.md"), "# Generated by MoonAP\n", "utf8");
  await fs.writeFile(path.join(projectDir, "cmd", "main", "moon.pkg"), mainPackageJson(), "utf8");
  await fs.writeFile(path.join(projectDir, "cmd", "main", "main.mbt"), moonbitCode, "utf8");
}

function runMoonBuild(projectDir) {
  return new Promise((resolve, reject) => {
    const child = spawn("moon", ["build", "cmd/main", "--target", "wasm"], {
      cwd: projectDir,
      env: process.env,
      stdio: ["ignore", "pipe", "pipe"],
    });

    let stdout = "";
    let stderr = "";

    child.stdout.on("data", (chunk) => {
      stdout += chunk.toString();
    });

    child.stderr.on("data", (chunk) => {
      stderr += chunk.toString();
    });

    child.on("error", reject);
    child.on("close", (code) => {
      if (code === 0) {
        resolve({ stdout, stderr });
        return;
      }

      reject(new Error(`moon build failed with code ${code}\n${stdout}\n${stderr}`));
    });
  });
}

export async function compileMoonBitToWasm(moonbitCode) {
  await fs.mkdir(TEMP_ROOT, { recursive: true });
  const projectDir = await fs.mkdtemp(path.join(TEMP_ROOT, `${os.platform()}-`));
  await ensureTemplateFiles(projectDir, moonbitCode);
  const logs = await runMoonBuild(projectDir);
  const wasmPath = path.join(projectDir, "_build", "wasm", "debug", "build", "cmd", "main", "main.wasm");
  const wasmBuffer = await fs.readFile(wasmPath);

  return {
    projectDir,
    wasmBase64: wasmBuffer.toString("base64"),
    buildLog: [logs.stdout.trim(), logs.stderr.trim()].filter(Boolean).join("\n"),
  };
}
